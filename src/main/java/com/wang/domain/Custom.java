public class Custom {
    private int custom_id;
    private String custom_name;
    private String custom_photo;
    private String custom_password;
    private int custom_state;

    public int getCustom_id() {
        return custom_id;
    }

    public void setCustom_id(int custom_id) {
        this.custom_id = custom_id;
    }

    public String getCustom_name() {
        return custom_name;
    }

    public void setCustom_name(String custom_name) {
        this.custom_name = custom_name;
    }

    public String getCustom_photo() {
        return custom_photo;
    }

    public void setCustom_photo(String custom_photo) {
        this.custom_photo = custom_photo;
    }

    public String getCustom_password() {
        return custom_password;
    }

    public void setCustom_password(String custom_password) {
        this.custom_password = custom_password;
    }

    public int getCustom_state() {
        return custom_state;
    }

    public void setCustom_state(int custom_state) {
        this.custom_state = custom_state;
    }
}
